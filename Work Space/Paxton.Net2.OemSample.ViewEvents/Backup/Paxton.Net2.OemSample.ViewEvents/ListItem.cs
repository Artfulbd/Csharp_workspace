using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Paxton.Net2.OemSample.ViewEvents
{
	internal class ListItem
	{
		private int _listId;
		private string _listName;

		public ListItem(int itemId, string itemName)
		{
			_listId = itemId;
			_listName = itemName;
		}

		public int ListId
		{
			get
			{
				return _listId;
			}
		}

		public string DisplayName
		{
			get
			{
				return _listName;
			}
		}
	}
}
